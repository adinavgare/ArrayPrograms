public class PrintElements 
{
    public static void main(String[] args) 
    {
        int arr[] = {8,5,9,4,3,1};

        System.out.println("The Array elements are: ");
        for(int i=0; i<arr.length; i++)
        {
            int n = arr[i];
            System.out.print(n + ", ");
        }
        
    }//end main
    
}//end class
